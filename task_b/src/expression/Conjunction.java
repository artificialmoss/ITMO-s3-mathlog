package expression;

import utils.VariableManager;

import java.util.Objects;

public class Conjunction implements Expression {

    private Expression left; //Conjunction
    private Expression right; //Negation

    public Conjunction(Expression left, Expression right) {
        this.left = left;
        this.right = right;
    }

    public Expression getLeft() {
        return left;
    }

    public Expression getRight() {
        return right;
    }

    @Override
    public String toTree() {
        return "(&," + left.toTree() + "," + right.toTree() + ")";
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Conjunction that = (Conjunction) o;
        return Objects.equals(left, that.left) &&
                Objects.equals(right, that.right);
    }

    @Override
    public int hashCode() {
        return Objects.hash(left, right);
    }

    @Override
    public String toString() {
        return "(" + left.toString() + "&" + right.toString() + ")";
    }

    @Override
    public boolean getValue() {
        return left.getValue() && right.getValue();
    }

    @Override
    public void setValue(VariableManager vm) {
        left.setValue(vm);
        right.setValue(vm);
    }
}
